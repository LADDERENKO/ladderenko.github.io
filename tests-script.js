function startTest(quizId) {
    const quiz = quizzes[quizId];
    if (!quiz) return;
    document.getElementById('test-list-section').style.display = 'none';
    const section = document.getElementById('quiz-section');
    section.style.display = 'block';
    document.getElementById('test-title').innerText = quiz.title;
    const form = document.getElementById('quiz-form');
    form.innerHTML = '';
    quiz.questions.forEach((q, i) => {
        const div = document.createElement('div');
        div.className = 'question-box';
        div.innerHTML = `<p style="color:#c988ef; font-weight:bold; margin-bottom:10px;">${i+1}. ${q.q}</p>`;
        q.a.forEach((opt, j) => {
            const lbl = document.createElement('label');
            lbl.className = 'option-item';
            lbl.innerHTML = `<input type="radio" name="q${i}" value="${j}"> ${opt}`;
            div.appendChild(lbl);
        });
        form.appendChild(div);
    });
    window.currentQuiz = quizId;
    window.scrollTo(0,0);
}

function submitTest() {
    const quiz = quizzes[window.currentQuiz];
    let score = 0;
    quiz.questions.forEach((q, i) => {
        const sel = document.querySelector(`input[name="q${i}"]:checked`);
        const boxes = document.querySelectorAll('.question-box');
        if (sel && parseInt(sel.value) === q.c) {
            score++;
            boxes[i].style.borderColor = "#00ff80";
        } else {
            boxes[i].style.borderColor = "#ff4444";
        }
    });
    const res = document.getElementById('result');
    res.className = 'result-box';
    res.innerHTML = `<h2>ВАШ СЧЕТ: ${score}/5</h2><button class="cta-button" onclick="location.reload()">НАЗАД</button>`;
}